diccionario = { 'a':1, 'b':2, 'c':3, 'd':4, 'e':5, 'f':6, 'g':7, 'h':8 }

# Obtener todas las llaves del diccionario
resultado = diccionario.keys()
print(resultado)

# Convertir las llaves a tupla
resultado = tuple(resultado)
print(resultado)

# Obtener todos los valores de un diccionario
resultado = diccionario.values()
print(resultado)

# Convertir los valores a lista
resultado = list(resultado)
print(resultado)

# Obtener todos los elementos del diccionario
resultado = diccionario.items()
print(resultado)

# Convertir los elementos a lista
resultado = list(resultado)
print(resultado)
