# VIDEO - Valores por índices

# -Las tuplas a diferencia de las listas son inmutables 

tupla = (1,2,3,4,5,6,7,8,9,0)

# Obtener elemento de la tupla
elemento = tupla[4]
print(elemento)

# Obtener elemento de la tupla con número negativo
elemento = tupla[-1]
print(elemento)

sub = tupla[0:9:2]
print(sub)


