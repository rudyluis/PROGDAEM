# VIDEO - Cadena de caracteres

# -Los strings son cadenas de caracteres que ya tienen un orden establecido 
# -Los strings son inmutables

curso = "Curso de Python"

# Obtener los caracteres con lo que cuenta
resultado = len(curso)
print(resultado)

# Obtener la primera letra
resultado = curso[0]
print(resultado)

# Obtener un string con saltos 
resultado = curso[1:6:2]
print(resultado)

